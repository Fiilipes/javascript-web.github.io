document.getElementById("copy_icon1").addEventListener("click", function(){
    var text = document.getElementById("code1").textContent
    navigator.clipboard.writeText(text)
    document.getElementById("code1").style.opacity = 0.3
    document.getElementById("code1").style.opacity = "#222"
    document.getElementById("copy_text1").style.opacity = 1



    let counterrr = 1;
    const k = setInterval(function(){
        console.log(counterrr);

        counterrr++
        if (counterrr == 4) {
            document.getElementById("code1").style.opacity = 1
            document.getElementById("code1").style.opacity = "#111"
            document.getElementById("copy_text1").style.opacity = 0

        }
}, 500);   









});
